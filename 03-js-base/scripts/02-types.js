/**
 * ТИПЫ ДАННЫХ
 *
 * Значение в JavaScript всегда относится к данным определённого типа.
 * Например, это может быть строка или число.
 */

// Число | Number

let n = 10;
n = 12.5;

// Строка | String

let message = "Привет";
message = 'Одинарные кавычки';

// Булевый (логический) тип | Boolean

let isGreater = true; // false
isGreater = 2 > 3;
console.log(isGreater);

// Undefined

let age;
console.log(age);

// Оператор typeof

typeof 0
typeof true
typeof "foo"

let a = 2;
console.log(typeof a);

a = "Hello";
console.log(typeof a);
